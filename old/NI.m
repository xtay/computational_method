function [ yN ] = NI( x, y, n, xN, yd )
%本算法使用已经计算出的Newton多项式系数计算对于xN的插值结果yN
%ref page 92. 事实上，这里的变量较书上给出的算法有点改动，为的是能够使用已经计
%算好的yd，而不是每次插值都要重新计算yd，这需要使用者调用本函数前自己主动对离散
%点调用DD算法生成Newton多项式系数。
%输入：
%   x,y, 进行插值的若干点。这是y其实不会被用到
%   n, 分段数目，即插值点的数目总共有n+1个
%   xN, 待计算点的x坐标
%   yd, 调用DD函数获得的Newton插值多项式的系数
%输出：
%   yN, 由Newton插值多项式计算得出的对应于横坐标xN的纵坐标上的近似值
yN = yd(n+1);
for i = n:-1:1
    yN = yN*(xN - x(i)) + yd(i);
end

end